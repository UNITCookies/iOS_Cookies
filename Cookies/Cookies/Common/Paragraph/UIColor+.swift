import UIKit

extension UIColor {
    
    @nonobjc class var main: UIColor {
        return UIColor(red: 38.0/255.0, green: 38.0/255.0, blue: 38.0/255.0, alpha: 1.0)
    }
    
    @nonobjc class var sub: UIColor {
        return UIColor(red: 241.0/255.0, green: 232.0/255.0, blue: 217.0/255.0, alpha: 1.0)
    }
    
    @nonobjc class var black: UIColor {
        return UIColor(red: 0.0/255.0, green: 0.0/255.0, blue: 0.0/255.0, alpha: 1.0)
    }
    
    @nonobjc class var gray: UIColor {
        return UIColor(red: 163.0/255.0, green: 163.0/255.0, blue: 163.0/255.0, alpha: 1.0)
    }
    
    @nonobjc class var white: UIColor {
        return UIColor(red: 255.0/255.0, green: 255.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
    
}
 
